#### Operators in Python

""" ============= Arithmetic Operators ================ """
a = 10; b = 19

#1 Addition
print("Addition:", a + b)  # 29

#2 Subtraction
print("Subtraction:", a - b)  # -9

#3 Multiplication
print("Multiplication:", a * b)  # 190

#4 Division
print("Division:", a / b) # 0.5263157894736842

#5 Floor Division
print("Floor Division:", a // b) # 0

#6 Modulo
print("Modulo:", a % b) # 10

#7 Exponentiation
print("Exponentiation:", a ** 2) # 100

