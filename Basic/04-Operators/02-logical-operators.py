#### Operators in Python

""" ============= Logical Operators ================ """
a = True; b = False

#1 Logical AND
print("Logical AND:", a and b ) # False

#2 Logical OR
print("Logical OR:", a or b) # True

#3 Logical NOT
print("Logical NOT a:", not a) # False
print("Logical NOT b:", not b) # True

