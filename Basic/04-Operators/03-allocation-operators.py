#### Operators in Python

""" ============= Allocation Operators ================ """
a = 10; b = 12

#1 Addition Assignment
a += b  # a = a + b
print("Addition Assignment:", a)  # 22

#2 Subtraction Assignment
a -= b  # a = a - b
print("Subtraction Assignment:", a)  # 10

#3 Multiplication Assignment
a *= b  # a = a * b
print("Multiplication Assignment:", a)  # 120

#4 Division Assignment
a /= b  # a = a / b
print("Division Assignment:", a)  # 10.0

#5 Floor Division Assignment
a = 23; b = 5
a //= b  # a = a // b
print("Floor Division Assignment:", a)  # 4

#6 Modulo Assignment
a %= b  # a = a % b
print("Modulo Assignment:", a)  # 4

#7 Exponentiation Assignment
a **= 2  # a = a ** 2
print("Exponentiation Assignment:", a)  # 16

