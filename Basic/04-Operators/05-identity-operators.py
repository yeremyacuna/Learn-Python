#### Operators in Python

""" ============= Identity Operators ================ """
a = [1, 2, 3]
b = a
c = [1, 2, 3]

#1 Is Operator
print("Is Operator (a is b):", a is b)  # True
print("Is Operator (a is c):", a is c)  # False

#2 Is Not Operator
print("Is Not Operator (a is not b):", a is not b)  # False
print("Is Not Operator (a is not c):", a is not c)  # True
