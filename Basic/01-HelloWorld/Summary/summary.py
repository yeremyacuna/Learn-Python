# ----------------------------------------
# 1. Basic print statement
# ----------------------------------------

print("Hello, World!")


# ----------------------------------------
# 2. Explicit concatenation using +
# ----------------------------------------

print("Hello" + " " + "World")


# ----------------------------------------
# 3. Implicit string concatenation
# Python automatically joins adjacent string literals
# ----------------------------------------

print("Hello" " World")


# ----------------------------------------
# 4. Using newline character \n
# \n creates a line break inside the string
# ----------------------------------------

print("My\nName\nis\nYeremy")


# ----------------------------------------
# 5. Multiple print() calls
# Each print() adds a newline automatically
# ----------------------------------------

print("Python is")
print("awesome!")