#### Hello World in Python

## Here we create a "hello world" using a string with single quotes and concatenation with a plus sign.
print('Hello' + 'World') 

## Here we create a "hello world" using a string with single quotes and concatenation without a plus sign.
print('Hello' 'World') 

## Here we create it using a string with single quotes and concatenation with a plus sign and a newline function.
print('My' + '\nName' + '\nis' '\nYeremy')

## Here we create it using a string with double quotes and concatenation with a plus sign.
print("Hello" + "World")

## Here we create it using a string whit double and single quotes and two print functions.
## This makes the output on two lines.
print('Python is')
print("awesome!")